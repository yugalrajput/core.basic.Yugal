package com.rays.basics;

public class Logical {
//	logical&& if : else
public static void main(String[]args) {
	int a=10;
	int b=10;
	if(a<0&&b<0) {
		System.out.println("yugal rajput");
	}else {
		System.out.println("hello");
	}
	
//logical||
	int c=10;
	int d=10;
	if(c>11||d>11) {
		System.out.println("ravi");
		
	}else {
		System.out.println("neha");
	}
}
}
