package com.rays.basics;

public class TestForLoop1 {
	public static void main(String[] args) {

		for (int i = 1; i <= 5; i++) {

			System.out.println(i + "yugal");
		}
		for (int j = 5; j > 1; j--) {

			System.out.println(j + "rajput");
		}
		for (int k = 3; k <= 7; k++) {

			System.out.println(k + "aman");
		}
		for (int l = 9; l <= 13; l++) {

			System.out.println(l + "hani");

		}
		for (int m = 5; m > 1; m--) {

			System.out.println(m + "raman");
		}

	}
}
